# 图片所在的文件夹
picPath = 'E:/我的资料/笔记/日记/记录'

# 目标文件夹
toPath = 'E:/我的资料/笔记/日记/记录/240207vue笔记'

# 遍历图片列表，将每个图片从源文件夹复制到目标文件夹
for pic in piclist:
    try:
        # 将URL编码中的%20转换为空格
        pic = urllib.parse.unquote(pic)
        # 创建目标路径中的子文件夹
        os.makedirs(os.path.dirname(toPath + '/' + pic), exist_ok=True)
        # 复制文件
        shutil.copy(picPath + '/' + pic, toPath + '/' + pic)
        # 删除原文件
        os.remove(picPath + '/' + pic)
    except Exception as e:
        print(f"处理图片 {pic} 时出错: {str(e)}")
        
